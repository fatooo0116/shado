		<p>豊かな自然に囲まれた小豆島。瀬戸内海特有の年間を通して雨が少なく、温暖な気候に恵まれたこの島では、伝統ある様々な特産品が生まれてきました。<br />10月初旬から3月にかけて島を歩けば、細く延ばしたそうめんを吊るし、日光のもとで乾燥させている風景を目にすることができます。そうめんの帯が爽やかな風になびいている様子は、小豆島の風物詩にもなっています。<br />この風景、実に400年前の江戸時代初期から続いています。小豆島の手延べそうめんは、昔から今に至るまで変わらぬ製法で、職人の手により守り続けられてきました。手間を惜しまぬ製法で作られた手延べそうめんは、そのおいしさから非常に有名で、小豆島は全国三大産地の一つとして位置づけられているほどです。<br />また歩を進めてみます。すると、朱色の旗をなびかせた建屋が立ち並ぶ様子が見受けられます。旗には「醤」という文字。この一帯は、「醤（ひしお）の郷」と呼ばれ、醤油蔵や佃煮屋が軒を連ねており、今では観光地にもなっている場所です。</p>


		<p>小豆島の醤油づくりの歴史も古く、手延べそうめんと同じく約400年前の江戸時代にまで遡ります。その当時より、小豆島は海上交通の要所であったことから、大阪などの大消費地への海運にも恵まれていました。また、小豆島の気候は醤油づくりに欠かせない麹の発酵に適していたことから、醤油づくりは発展し、「島醤油」と呼ばれるまでとなりました。そして、この伝統の島醤油を生かして作られたのが、小豆島の佃煮です。更に、当初は材料として小豆島産の芋の蔓（つる）を使用していました。まさに小豆島の風土が生んだ、郷土愛に溢れた特産品であると言えるでしょう。</p>
        
        <p>小豆島の特産品は、手延べそうめん、醤油、佃煮の他にも、オリーブやハーブ、電照菊、すもも等が挙げられ、小豆島特有の気候風土があってこそ生まれてきたものです。ただ、それらの発展を紐解けば、先人の先見の明や開拓精神、生産努力に郷土愛、そして、本物をつくるために伝統の技法貫いてきた職人達の営みが混然となって今に存在していることがわかります。</p>
		
		<p>小豆島と出逢った私達は、これらの本物を作ろうという姿勢に共感し、小豆島の特産品そのものだけでなく、今に至るまで守り続けられてきた歴史や営みまでも大切にしていきたいと考えています。時代が変わっても、人の手でしか追い求められない、もしくは作り出せないものがあると感じているからこそ、これからも小豆島の歴史や営みが続いていくことを願っています。そのためにも、より多くの人に小豆島のことを知ってもらいたい、本サイトはそのきっかけとなればと思っております。</p>
        
